interface PixelCloverProps {
  leaves: 3 | 4;
}

export default function PixelClover({ leaves }: PixelCloverProps) {
  // Use the target grid to determine visibility
  const targetGrid = leaves === 4 ? FOUR_LEAF : THREE_LEAF;

  return (
    <svg
      width={COLS * CELL}
      height={ROWS * CELL}
      viewBox={`0 0 ${COLS * CELL} ${ROWS * CELL}`}
      aria-label={`${leaves}-leaf clover`}
    >
      {Array.from({ length: ROWS }).map((_, y) =>
        Array.from({ length: COLS }).map((_, x) => {
          const fourLeafVal = FOUR_LEAF[y][x];
          const threeLeafVal = THREE_LEAF[y][x];

          // If neither grid has a pixel here, render nothing
          if (fourLeafVal === 0 && threeLeafVal === 0) return null;

          const targetVal = targetGrid[y][x];
          const isVisible = targetVal !== 0;

          // Determine color:
          // If visible, use the target color.
          // If invisible (fading out), use the 'other' grid's color so it fades out with the correct color.
          const colorVal = isVisible
            ? targetVal
            : leaves === 4
              ? threeLeafVal
              : fourLeafVal;
          
          const color = COLORS[colorVal] || COLORS[1]; // Fallback to avoid crash if 0

          return (
            <rect
              key={`${x}-${y}`}
              x={x * CELL}
              y={y * CELL}
              width={CELL}
              height={CELL}
              fill={color}
              opacity={isVisible ? 1 : 0}
              style={{ transition: "opacity 0.6s ease-in-out" }}
            />
          );
        }),
      )}
    </svg>
  );
}
